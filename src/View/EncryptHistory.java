    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        Dbcon dbcon = new Dbcon();
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        try {

            ResultSet rs = dbcon.select("select * from tbl_file_process_history order by history_id asc");
            while (rs.next()) {
                String algo = rs.getString(6);
                int algorithm = Integer.parseInt(algo);
                if (algorithm == 1) {
                    algo = "DES";
                } else if (algorithm == 2) {
                    algo = "TDES";
                } else {
                    algo = "RSA";
                }
                dt.addRow(new String[]{rs.getString(1), rs.getString(8), algo});
            }
            jTable1.setModel(dt);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

