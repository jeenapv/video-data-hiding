    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        String id = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
        Dbcon dbcon = new Dbcon();
        ResultSet rs = dbcon.select("select * from tbl_file_process_history where history_id='" + id + "'");
        try {
            if (rs.next()) {
                String name = rs.getString("cipher_file_name");
                jTextField1.setText(name);
                String size = rs.getString(7);
                jTextField2.setText(size);
                String algo = rs.getString(6);
                int algorithm_id = Integer.parseInt(algo);
                if (algorithm_id == 1) {
                    algo = "DES";
                } else if (algorithm_id == 2) {
                    algo = "TDES";
                } else {
                    algo = "RSA";
                }
                jTextField4.setText(algo);
                String password = rs.getString(4);
                jPasswordField1.setText(password);
                encypt_size_text.setText(rs.getString("cipher_file_size") + " Kb");
                jTextField5.setText("");
                String encryption_file_type = rs.getString("encryption_file_type");
                if (encryption_file_type.equals("0")) {
                    hidden_data_text.setSelected(true);
                } else {
                    hidden_data_file.setSelected(true);
                }
                outputCipherFile = new File(Configuration.videoPool + name);
                send_button.setEnabled(true);
                Configuration.setDefaultFileIcon(outputCipherFile, jLabel2);
                history_id = Integer.parseInt(id.trim());
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jTable1MouseClicked

